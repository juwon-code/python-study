# 원뿔의 부피와 겉넓이를 출력하는 프로그램.
# 반지름과, 높이 값 할당.
radius = 20
height = 30

# 부피 출력.
print(1 / 3 * 3.14 * radius ** 2 * height)

# 겉넓이 출력.
print(3.14 * radius ** 2 + 3.14 * radius * height)

# 정수 나눗셈 연산
print(5 // 2)


# 내장함수 예시.
# 최댓값 구하기.
print(max(2, 3, 4))

# 최솟값 구하기.
print(min(2, 3, 4))

# 절댓값 구하기.
print(abs(-3))

# 반올림 구하기.
print(round(3.14))

# 거듭제곱 구하기.
print(pow(2, 3))

